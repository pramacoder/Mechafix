    // Add scroll animations
    const timelineItems = document.querySelectorAll('.relative.flex');

    const observerOptions = {
        threshold: 0.3,
        rootMargin: '0px 0px -50px 0px'
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateY(0)';
            }
        });
    }, observerOptions);

    // Initialize timeline items with opacity 0 and slight translation
    timelineItems.forEach(item => {
        item.style.opacity = '0';
        item.style.transform = 'translateY(30px)';
        item.style.transition = 'opacity 0.6s ease, transform 0.6s ease';
        observer.observe(item);
    });

    // Add click handlers for images
    document.querySelectorAll('img').forEach(img => {
        img.addEventListener('click', function() {
            // Simple modal functionality could be added here
            console.log('Image clicked:', this.alt);
        });
    });

    // Add hover effects to timeline dots
    document.querySelectorAll('.bg-gray-900.rounded-full').forEach(dot => {
        dot.addEventListener('mouseenter', function() {
            this.style.transform = 'translate(-50%, -50%) scale(1.2)';
            this.style.transition = 'transform 0.3s ease';
        });
        dot.addEventListener('mouseleave', function() {
            this.style.transform = 'translate(-50%, -50%) scale(1)';
        });
    });
</script> 